//вывести числа от 10 до 0


// c While
function getNumbersFrom10to0() {
	let i = 10;
	while (i) { alert(i);
		i--;
}
}
 //getNumbersFrom10to0();


// c do While
function getNumbersFrom10to0() {
	let i = 10;
	do {
		alert(i); 
		i--
	} while (i );

}
 //getNumbersFrom10to0();


// c FOR
function getNumbersFrom10to0() {
	for (let i = 10; i>0; i--) { alert(i); }
}

